    <h2 class="page-content__subtitle">
        Социальная ответственность
    </h2>
    <p class="page-content__text">
        Давайте теперь поговорим о социальной ответственности. Она выражается в ясном понимании влияния, которое оказывают строительные отходы на окружающий нас мир, в котором будут жить следующие поколения наших детей.
    </p>
    <p class="page-content__text">
        Строительный мусор, согласно международной классификации по химическом опасности, является наименее вредоносным. Определенные типы красочных субстанций, которые применяются в процессе строительства, относятся к более токсичным категориям.<br>
        Микрочастицы, которые повисают в воздухе в результате проведения строительных работ несут наибольший разрушительный эффект на состояние слизистой оболочки легких. Чем больше строительных отходов находится на территории застройки или сноса, тем выше концентрация строительной пыли на один квадратный метр пространства, что ведет к ее более интенсивному оседанию в легких работников. Несвоевременный вывоз строительного мусора может косвенно привести к ухудшению состояния здоровья трудящихся и привести к неблагоприятным последствиям для организации. Во избежания наступления негативных событий, настоятельно просим Вам вывозить мусор вовремя.
    </p>
    <p class="page-content__text">
        Деревянные материалы (окна, брусья) часто имею на своей поверхности напыления лака, материалов против гниения и при сжигании в отдаленных областях, а не в специальных свалках выделяют токсичные смеси, которые пагубно влияют на окружающую атмосферу.<br>
        Если же строительный мусор утилизирован в ненадлежащем месте, то при сгорании или закапывании в яму часть отходов со временем попадает в подземные воды, которые затем втекают в водохранилища, из которых нас снабжают питьевой водой. Также этой водой осуществляют полив овощей, фруктов и корнеплодов, которые мы и наши дети употребляем в пищу. Для того, чтобы соответствовать стандартам корпоративной ответственности и преследовать философию sustainable development мы должны ответственно относится к сохранению пригодных условий жизни для следующих поколений.
    </p>
