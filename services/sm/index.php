        Вывоз строительного мусора
    </h1>
    <p class="page-content__text">
        Строительный мусор представляет собой физические материалы, которые подлежат утилизации. Строительные отходы материализуются после проведения строительных работ или же после разрушения (в частности, сноса) конструкций разных типов. Яркими примерами отходов строительной деятельности являются фрагменты гипсокартонных панелей, остатки керамических блоков, кирпичный бой, фрагменты металлоконструкций различной этиологии, деревянные остатки (бруски, палочки и фрагменты нестандартной формы), фрагменты линолеумового настила, части оконных рам, мусор из раздробленных плит гипсокартонная. Отходы такого типа - это неизбежность во время проведения строительных или демонтажных работ.
