    <p class="page-content__text">
        Мы с Вами познакомились с определением строительного мусора. Далее логическая цепь приводит нас к различным его типам.
    </p>
    <p class="page-content__text">
        Первый тип отходов образуется в результате подготовки территории, помещения. Следующие мероприятия имеют место: зачистка территории, демонтаж структур, которые будут сноситься безвозвратно или же частично заменяться на новые. К сожалению, отходы такого типа крайне громоздки и весомы, что и обуславливает их обязательную утилизацию до начала любых строительных процессов.
    </p>
    <p class="page-content__text">
        Второй тип отходов можно охарактеризовать как материалы, которые не представляют дальнейшей ценности и образуют помеху для дальнейших фаз проекта. Такой мусор носит динамический характер и подлежит вывозу через некоторые промежутки времени (обычно по мере накапливания оного в на территории выделенной под строительство или демонтаж).
    </p>
    <p class="page-content__text">
        Третья категория мусора, которая материализуется после завершающей фазы строительных работ, не носит срочных характер вывоза. Такой мусор также является неизбежным отходом, который должен быть утилизирован. Зачастую, строительные компании пытаются утилизировать мусор такого типа самостоятельно, сбрасывая его не в специализированные места, а в общественные контейнеры, тем самым навлекая на себя дополнительные расходы в форме административного нарушения (финансовые санкции налагаются на компании в обязательном порядке). Во избежание наступления столь нежелательных сценариев развития событий, которые кроме финансовых потерь несут общий негатив бизнесу и портят его имидж (фирма заносится в реестр проштрафившихся организаций), настоятельно рекомендуем Вам минимальными усилиями оформить услугу по вывозу мусора в нашей компании.
