        <li class="page-content__ul-item">
            фрагменты деревьев (стволовая часть дерева, корневая часть дерева, спилы веток);
        </li>
        <li class="page-content__ul-item">
            громоздкая фурнитура, которая не может быть разобрана без значительных усилий;
        </li>
        <li class="page-content__ul-item">
            громоздкие куски бетона;
        </li>
        <li class="page-content__ul-item">
            дверные коробки и рамы;
        </li>
        <li class="page-content__ul-item">
            булыжники и каменные плиты;
        </li>
        <li class="page-content__ul-item">
            бетонные и кирпичные перегородки, которые подлежат утилизации после разрушения.
